n1 = float(input())
n2 = float(input())

soma = n1 + n2
multiplicacao = n1 * n2
divisao = n1 / n2
print(f'a soma dos inteiros é: {soma}')
print(f'a concatenaçao das strings : {n1}{int(n2)}')
print(f'a multiplicaçao dos valores: {multiplicacao}')
print(f'a multiplicação desses valores como strings é: {str(n1) * int(n2)}')
print(f'a divisão dos valores é: ',divisao)
print(f'divisão inteira dos valores é: ',n1 // n2)
print(f'exponenciação desses valores é: ',n1 ** n2)
print(f'o módulo (resto) desses valores e igual á: ',n1 % n2)
